const BANNER = '⚙ generated by bin/render-docs.mjs from source — do not edit by hand (run `npm run render-docs`)';
const esc = (s) => String(s ?? '').replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

// ───────────────────────── pure: gather ─────────────────────────

/** Gather an organ's live data: real component list (from source) + counts. Pure given `reader`. */
export function gatherOrgan(meta, reader) {
  const components = [];
  for (const src of meta.sources || []) {
    for (const name of reader.list(src.root, src)) {
      components.push({ name, kind: src.kind, desc: (meta.descriptions || {})[name] || '' });
    }
  }
  for (const c of meta.componentsLiteral || []) components.push(c);
  const counts = {};
  for (const c of meta.counts || []) {
    if (c.json) {
      const obj = reader.readJson(c.root, c.path);
      for (const [k, path] of Object.entries(c.map)) counts[k] = path.split('.').reduce((o, p) => (o == null ? o : o[p]), obj);
    } else if (c.grep) {
      const txt = reader.readText(c.root, c.path);
      for (const [k, re] of Object.entries(c.grep)) { const m = txt.match(new RegExp(re)); counts[k] = m ? m[1] : '?'; }
    }
  }
  return { ...meta, components, counts };
}

