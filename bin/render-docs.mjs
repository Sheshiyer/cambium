// Cambium — render-docs: generate the per-organ pages + the catalog FROM SOURCE.
//
// HOMEOSTASIS applied to the docs themselves: the component lists + counts are read
// from the LIVE organ repos, so the docs cannot drift. Only the editorial bits
// (emoji / tagline / homeostasis role / contract / spend) live in organs.meta.json.
//
//   npm run render-docs          → (re)write docs/organs/*.html + docs/organs.html
//   npm run render-docs:check    → re-render + diff vs on-disk; exit 1 on drift (the V-check)
//
// Zero-dep, Node ESM. Pure core (gatherOrgan/renderOrganPage/renderCatalog) + a thin
// fs reader injected at the edge, so it is fully testable without the repos present.

import { readFileSync, readdirSync, writeFileSync, existsSync } from 'node:fs';
