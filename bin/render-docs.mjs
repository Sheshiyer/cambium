// ───────────────────────── I/O shell ─────────────────────────

function makeReader(cambiumRoot, roots) {
  const base = (root, p) => join(cambiumRoot, '..', roots[root], p);
  return {
    list(root, src) {
      const dir = base(root, src.dir);
      if (!existsSync(dir)) return [];
      const entries = readdirSync(dir, { withFileTypes: true });
      if (src.type === 'dirs') {
        return entries.filter((e) => e.isDirectory()).map((e) => e.name).filter((n) => !(src.exclude || []).includes(n)).sort();
      }
      return entries
        .filter((e) => e.isFile() && e.name.endsWith(src.ext) && !e.name.includes('.test'))
        .map((e) => e.name.slice(0, -src.ext.length))
        .filter((n) => !(src.exclude || []).includes(n))
        .sort();
    },
    readJson: (root, p) => { const f = base(root, p); return existsSync(f) ? JSON.parse(readFileSync(f, 'utf8')) : {}; },
    readText: (root, p) => (existsSync(base(root, p)) ? readFileSync(base(root, p), 'utf8') : ''),
  };
}

export function main(argv, root) {
  const check = argv.includes('--check');
  const meta = JSON.parse(readFileSync(join(root, 'docs', 'organs', 'organs.meta.json'), 'utf8'));
  const reader = makeReader(root, meta.roots);
  const all = meta.organs.map((o) => gatherOrgan(o, reader));
  const outputs = [
    ...all.map((d) => ({ path: join(root, 'docs', 'organs', `${d.id}.html`), html: renderOrganPage(d) })),
    { path: join(root, 'docs', 'organs.html'), html: renderCatalog(all) },
  ];
  if (check) {
    const drifted = outputs.filter((o) => !existsSync(o.path) || readFileSync(o.path, 'utf8') !== o.html);
    if (drifted.length) {
      console.log(`✗ docs DRIFT — ${drifted.length} page(s) out of sync with source:`);
      for (const d of drifted) console.log(`    ${d.path.replace(root + '/', '')}`);
      console.log('  run `npm run render-docs` to regenerate.');
      return 1;
    }
    console.log(`✓ docs in sync with source (${outputs.length} pages, ${all.reduce((n, d) => n + d.components.length, 0)} components)`);
    return 0;
  }
  for (const o of outputs) writeFileSync(o.path, o.html);
  console.log(`✓ rendered ${outputs.length} pages from source — ${all.map((d) => `${d.id}(${d.components.length})`).join(' · ')}`);
  return 0;
}

